var num = [5, 6, 9, 3, 4, 7] // arrays aleatorio

num.sort() // método sort() serve para ordenar em ordem crescente, usando o UNICODE

document.write(`${num[0]} <br>`)
document.write(`${num[1]} <br>`)
document.write(`${num[2]} <br>`)
document.write(`${num[3]} <br>`)
document.write(`${num[4]} <br>`)
document.write(`${num[5]} <br>`)
document.write(`<br>`)

num.reverse() // o método reverse() inverte a ordem, do final para o começo e do começo para o final em diante.

document.write(`${num[0]} <br>`)
document.write(`${num[1]} <br>`)
document.write(`${num[2]} <br>`)
document.write(`${num[3]} <br>`)
document.write(`${num[4]} <br>`)
document.write(`${num[5]} <br>`)
